# Генератор лотерейних чисел

import random


# Головна функція.
def main():
    # Ініціалізація списку.
    numbers = [0] * 7

    # Отримання випадкових чисел і присвоєння їх елементам списку.
    for i in range(7):
        numbers[i] = random.randint(0, 9)

    # Виводимо результати на екран.
    for i in range(7):
        print(numbers[i], end=' ')


# Виклик головної функції.
main()
